async def generate_security_configs(state: DevOpsGenState) -> dict:
    svc_lower = state["service_name"].lower()
    sonar = textwrap.dedent(f"""\
        # sonar-project.properties for {state['service_name']}
        sonar.projectKey={svc_lower}
        sonar.projectName={state['service_name']}
        sonar.sources=src/{svc_lower}
        sonar.exclusions=**/*Tests*/**,**/Migrations/**,**/wwwroot/**
        sonar.coverage.exclusions=**/*Tests*/**
        sonar.cs.opencover.reportsPaths=test-results/coverage.opencover.xml
        sonar.qualitygate.wait=true
    """)
    trivy = textwrap.dedent(f"""\
        # .trivyignore for {state['service_name']}
        # Add CVE exceptions here with justification
        # Example: CVE-XXXX-XXXXX  # Not applicable — not using affected feature

        # trivy.yaml
        scan:
          security-checks:
            - vuln
            - config
            - secret
        severity:
          - CRITICAL
          - HIGH
        exit-code: 1
        ignore-unfixed: false
        timeout: 10m
    """)
    return {"sonarqube_config": sonar, "trivy_config": trivy}


def _build_graph():
    g = StateGraph(DevOpsGenState)
    for name, fn in [
        ("generate_gitlab_ci", generate_gitlab_ci),
        ("generate_helm_chart", generate_helm_chart),
        ("generate_kong_config", generate_kong_config),
        ("generate_argocd_app", generate_argocd_app),
        ("generate_security_configs", generate_security_configs),
    ]:
        g.add_node(name, fn)
    g.add_edge("generate_gitlab_ci", "generate_helm_chart")
    g.add_edge("generate_helm_chart", "generate_kong_config")
    g.add_edge("generate_kong_config", "generate_argocd_app")
    g.add_edge("generate_argocd_app", "generate_security_configs")
    g.add_edge("generate_security_configs", END)
    g.set_entry_point("generate_gitlab_ci")
    return g.compile()


_graph = _build_graph()

