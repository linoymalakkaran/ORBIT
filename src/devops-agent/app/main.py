class DevOpsGenState(TypedDict):
    project_id: str
    service_name: str
    service_type: str           # "dotnet" | "python" | "angular"
    has_frontend: bool
    gitlab_ci: str
    helm_chart: str
    kong_plugin_config: str
    argocd_app: str
    sonarqube_config: str
    trivy_config: str
    security_pipeline_stages: str


async def _llm(prompt: str, system: str = "You are a senior DevOps engineer.") -> str:
    r = await litellm.acompletion(
        model=settings.default_model,
        messages=[{"role": "system", "content": system}, {"role": "user", "content": prompt}],
        temperature=0.05,
    )
    return r.choices[0].message.content or ""


async def generate_gitlab_ci(state: DevOpsGenState) -> dict:
    svc = state["service_name"]
    svc_lower = svc.lower()
    service_type = state["service_type"]

    if service_type == "dotnet":
        build_cmd = "dotnet build --configuration Release"
        test_cmd = "dotnet test --configuration Release --logger junit --results-directory test-results"
        docker_context = f"src/{svc}"
    elif service_type == "angular":
        build_cmd = f"npx nx build {svc_lower} --configuration production"
        test_cmd = f"npx nx test {svc_lower} --ci"
        docker_context = f"src/{svc_lower}"
    else:  # python
        build_cmd = "pip install poetry && poetry install"
        test_cmd = "poetry run pytest --junitxml=test-results/junit.xml"
        docker_context = f"src/{svc_lower}"

    ci_yaml = textwrap.dedent(f"""\
        # GitLab CI/CD — {svc}
        # Generated by ORBIT DevOps Agent

        variables:
          SERVICE_NAME: "{svc_lower}"
          HARBOR_REGISTRY: "harbor.ai.adports.ae/orbit"
          IMAGE_TAG: "$CI_COMMIT_SHA"
          SONAR_HOST_URL: "https://sonarqube.ai.adports.ae"
          ARGOCD_SERVER: "argocd.ai.adports.ae"

        stages:
          - lint
          - test
          - security-scan
          - build
          - push
          - trivy-scan
          - deploy-dev
          - deploy-prod

        # ── Lint ────────────────────────────────────────────────────────────
        lint:
          stage: lint
          image: harbor.ai.adports.ae/orbit/ci-tools:latest
          script:
            - echo "Linting {svc}..."
          rules:
            - if: '$CI_PIPELINE_SOURCE == "merge_request_event"'
            - if: '$CI_COMMIT_BRANCH == "main"'

        # ── Test ─────────────────────────────────────────────────────────────
        test:
          stage: test
          image: harbor.ai.adports.ae/orbit/ci-tools:latest
          script:
            - {build_cmd}
            - {test_cmd}
          artifacts:
            reports:
              junit: test-results/junit.xml
            expire_in: 7 days

        # ── SonarQube SAST ───────────────────────────────────────────────────
        sonarqube-scan:
          stage: security-scan
          image: harbor.ai.adports.ae/orbit/sonar-scanner:latest
          script:
            - sonar-scanner
              -Dsonar.projectKey={svc_lower}
              -Dsonar.sources=.
              -Dsonar.host.url=$SONAR_HOST_URL
              -Dsonar.login=$SONAR_TOKEN
              -Dsonar.qualitygate.wait=true
          allow_failure: false

        # ── Docker Build ─────────────────────────────────────────────────────
        build:
          stage: build
          image: harbor.ai.adports.ae/orbit/kaniko:latest
          script:
            - /kaniko/executor
              --context $CI_PROJECT_DIR/{docker_context}
              --dockerfile $CI_PROJECT_DIR/{docker_context}/Dockerfile
              --destination $HARBOR_REGISTRY/$SERVICE_NAME:$IMAGE_TAG
              --destination $HARBOR_REGISTRY/$SERVICE_NAME:latest
              --registry-mirror harbor.ai.adports.ae
          rules:
            - if: '$CI_COMMIT_BRANCH == "main"'

        # ── Trivy Container Scan ─────────────────────────────────────────────
        trivy-scan:
          stage: trivy-scan
          image: harbor.ai.adports.ae/orbit/trivy:latest
          script:
            - trivy image
              --exit-code 1
              --severity CRITICAL
              --no-progress
              $HARBOR_REGISTRY/$SERVICE_NAME:$IMAGE_TAG
          rules:
            - if: '$CI_COMMIT_BRANCH == "main"'

        # ── Deploy Dev ───────────────────────────────────────────────────────
        deploy-dev:
          stage: deploy-dev
          image: harbor.ai.adports.ae/orbit/argocd-cli:latest
          script:
            - argocd login $ARGOCD_SERVER --auth-token $ARGOCD_TOKEN --insecure
            - argocd app set {svc_lower}-dev --kustomize-image $HARBOR_REGISTRY/$SERVICE_NAME:$IMAGE_TAG
            - argocd app sync {svc_lower}-dev --prune --timeout 300
          environment:
            name: dev
          rules:
            - if: '$CI_COMMIT_BRANCH == "main"'

        # ── Deploy Prod (manual gate) ────────────────────────────────────────
        deploy-prod:
          stage: deploy-prod
          image: harbor.ai.adports.ae/orbit/argocd-cli:latest
          script:
            - argocd login $ARGOCD_SERVER --auth-token $ARGOCD_TOKEN --insecure
            - argocd app set {svc_lower}-prod --kustomize-image $HARBOR_REGISTRY/$SERVICE_NAME:$IMAGE_TAG
            - argocd app sync {svc_lower}-prod --prune --timeout 300
          environment:
            name: prod
          when: manual
          rules:
            - if: '$CI_COMMIT_BRANCH == "main"'
    """)
    return {"gitlab_ci": ci_yaml}


