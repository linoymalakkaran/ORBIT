class DevOpsGenRequest(BaseModel):
    project_id: str
    service_name: str
    service_type: str = "dotnet"   # dotnet | python | angular
    has_frontend: bool = False


@app.post("/api/generate/devops")
async def generate_devops(req: DevOpsGenRequest):
    """Generate full CI/CD pipeline, Helm chart, Kong config, ArgoCD app, security configs."""
    initial: DevOpsGenState = {
        "project_id": req.project_id,
        "service_name": req.service_name,
        "service_type": req.service_type,
        "has_frontend": req.has_frontend,
        "gitlab_ci": "",
        "helm_chart": "",
        "kong_plugin_config": "",
        "argocd_app": "",
        "sonarqube_config": "",
        "trivy_config": "",
        "security_pipeline_stages": "",
    }
    result = await _graph.ainvoke(initial)
    try:
        async with httpx.AsyncClient(timeout=5) as client:
            await client.post(f"{settings.ledger_url}/api/ledger", json={
                "project_id": req.project_id,
                "pipeline_run_id": f"devops-gen-{req.service_name}",
                "stage": "devops_generation",
                "actor": "devops-agent",
                "status": "success",
                "metadata": {"service_name": req.service_name, "service_type": req.service_type},
            })
    except Exception:
        pass
    return {
        "project_id": result["project_id"],
        "service_name": result["service_name"],
        "artifacts": {
            "gitlab_ci": result["gitlab_ci"],
            "helm_chart": result["helm_chart"],
            "kong_plugin_config": result["kong_plugin_config"],
            "argocd_app": result["argocd_app"],
            "sonarqube_config": result["sonarqube_config"],
            "trivy_config": result["trivy_config"],
        },
    }


